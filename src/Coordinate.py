class Coordinate(object):
    def __init__(self, x, y):
        self.x = x
        self.y = y

    def X(self):
        '''
        gibt X-Wert zurück
        :return: int
        '''
        return self.x

    def Y(self):
        '''
        gibt Y-Wert zurück
        :return: int
        '''
        return self.y

